#!/usr/bin/env python3
"""build_plan.py — render growth-plan.json to a self-contained HTML deliverable.

This is the ONLY presentation component and the artifact the founder actually
opens. It performs NO scoring and invents NO numbers: the `metrics` block is
read as authoritative precomputed script output (envelopes included) and
rendered verbatim with its provenance. The model assembles growth-plan.json
(positioning, calendar, scripts) around those script outputs, then runs this.

Compliance defense-in-depth: any calendar post marked promotional that lacks a
disclosure block gets the standard block stamped in by this renderer — the
deliverable can never contain an undisclosed promo post.

The rendered page also carries the community-dataset contribution banner
(config-gated via ui.contribution_reminder) — the nudge lives in the
deliverable the user opens, not in stderr.

CLI:
    build_plan.py growth-plan.json [--out growth-plan.html]
"""
