def esc(x: Any) -> str:
    return html.escape(str(x)) if x is not None else ""


def _fmt_value(m: dict[str, Any]) -> str:
    unit = m.get("unit") or ""
    if m.get("value") is None:
        return "no data"
    v = f"{unit}{m['value']}" if unit == "$" else f"{m['value']}{unit}"
    if m.get("low") is not None and m.get("high") is not None and m["low"] != m["high"]:
        lo = f"{unit}{m['low']}" if unit == "$" else f"{m['low']}{unit}"
        hi = f"{unit}{m['high']}" if unit == "$" else f"{m['high']}{unit}"
        return f"{v} (band {lo}–{hi})"
    return v


def render_positioning(plan: dict[str, Any]) -> str:
    pos = plan.get("positioning") or {}
    if not pos:
        return ""
    parts = [f'<div class="card"><h2 style="margin-top:0">Positioning</h2>']
    if pos.get("statement"):
        parts.append(f'<p class="v" style="font-size:16px">{esc(pos["statement"])}</p>')
    if pos.get("jtbd"):
        parts.append(f'<div class="k">Job to be done</div><p class="v">{esc(pos["jtbd"])}</p>')
    dun = pos.get("dunford") or {}
    if dun:
        parts.append("<ul>")
        for key in ("alternatives", "unique_attributes", "value", "icp", "category"):
            if dun.get(key):
                parts.append(f"<li><b>{esc(key.replace('_', ' ').title())}:</b> {esc(dun[key])}</li>")
        parts.append("</ul>")
    parts.append("</div>")
    return "".join(parts)


def render_pillars(plan: dict[str, Any]) -> str:
    pil = plan.get("pillars") or {}
    if not pil:
        return ""
    edu = float(pil.get("educational", 0)); ent = float(pil.get("entertainment", 0))
    pro = float(pil.get("promotional", 0))
    return (
        '<div class="card"><h2 style="margin-top:0">Content pillars</h2>'
        f'<div class="bar"><div style="width:{edu*100:.0f}%"></div>'
        f'<div style="width:{ent*100:.0f}%"></div><div style="width:{pro*100:.0f}%"></div></div>'
        f'<span class="pill edu">Educational {edu*100:.0f}%</span>'
        f'<span class="pill ent">Entertainment {ent*100:.0f}%</span>'
        f'<span class="pill promo">Promotional {pro*100:.0f}%</span>'
        "</div>"
    )


def render_calendar(plan: dict[str, Any]) -> str:
    weeks = plan.get("calendar") or []
    if not weeks:
        return ""
    out = ['<h2>Content calendar</h2>']
    pid = 0
    for wk in weeks:
        out.append(f'<div class="card"><h3 style="margin:0 0 8px">Week {esc(wk.get("week", "?"))}</h3>')
        for post in wk.get("posts", []):
            pid += 1
            pillar = str(post.get("pillar", "")).lower()
            cls = {"educational": "edu", "entertainment": "ent", "promotional": "promo"}.get(pillar, "")
            promo = bool(post.get("promotional")) or pillar == "promotional"
            out.append('<div class="post">')
            out.append(f'<h4>{esc(post.get("day", ""))} <span class="pill {cls}">{esc(post.get("pillar", ""))}</span></h4>')
            body_id = f"post{pid}"
            out.append(f'<div id="{body_id}">')
            for key, label in (("hook", "Hook (0–3s)"), ("value", "Value"), ("cta", "CTA"),
                               ("on_screen_text", "On-screen text"), ("spoken_keywords", "Spoken keywords")):
                if post.get(key):
                    out.append(f'<div class="k">{label}</div><div class="v">{esc(post[key])}</div>')
            tags = post.get("hashtags") or []
            if tags:
                out.append(f'<div class="k">Hashtags</div><div class="v">{esc(" ".join(tags))}</div>')
            out.append("</div>")
            if post.get("sound_guidance"):
                out.append(f'<div class="k">Sound</div><div class="v">{esc(post["sound_guidance"])}</div>')
            if promo:
                # Hard gate in depth: a promo post NEVER renders without the block.
                disc = post.get("disclosure") or disclosure_block()
                out.append(f'<div class="disc"><b>Mandatory disclosure:</b> {esc(disc)}</div>')
            out.append(f'<button data-copy="{body_id}">Copy script</button>')
            out.append("</div>")
        out.append("</div>")
    return "".join(out)


def render_metrics(plan: dict[str, Any]) -> str:
    metrics = plan.get("metrics") or []
    if not metrics:
        return ""
    rows = []
    for m in metrics:
        conf = esc(m.get("confidence", ""))
        flags = ", ".join(m.get("flags") or [])
        rows.append(
            "<tr>"
            f"<td>{esc(m.get('label', ''))}</td>"
            f"<td>{esc(_fmt_value(m))}</td>"
            f'<td class="conf-{conf}">{conf}</td>'
            f"<td>{esc(m.get('method', ''))}</td>"
            f"<td>{esc(', '.join(m.get('sources') or []))}</td>"
            f'<td class="flag">{esc(flags)}</td>'
            "</tr>"
        )
    return (
        '<div class="card"><h2 style="margin-top:0">Metrics — with provenance</h2>'
        "<table><tr><th>Metric</th><th>Value</th><th>Confidence</th><th>Method</th>"
        "<th>Sources</th><th>Flags</th></tr>" + "".join(rows) + "</table></div>"
    )


def render_compliance(plan: dict[str, Any]) -> str:
    comp = plan.get("compliance") or {}
    out = ['<div class="card"><h2 style="margin-top:0">Compliance checklist</h2><ul>']
    if comp.get("account_type"):
        out.append(f"<li><b>Account type:</b> {esc(comp['account_type'])}"
                   + (" — Commercial Music Library or original/owned audio ONLY"
                      if comp["account_type"] == "business" else "") + "</li>")
    if comp.get("category"):
        flag = comp.get("category_flag")
        out.append(f"<li><b>Category:</b> {esc(comp['category'])}"
                   + (f' — <span class="flag">{esc(flag)}</span>' if flag else " — no restriction flag") + "</li>")
    for note in comp.get("notes") or []:
        out.append(f"<li>{esc(note)}</li>")
    out.append(f"<li><b>Repurposing:</b> {esc(REPURPOSE_RULE)}</li>")
    out.append("</ul></div>")
    return "".join(out)


def render(plan: dict[str, Any]) -> str:
    product = plan.get("product") or {}
    title = product.get("name") or "Your product"
    variant_bits = [b for b in (product.get("variant"), product.get("why_variant")) if b]
    body = (
        render_positioning(plan)
        + render_pillars(plan)
        + render_calendar(plan)
        + render_metrics(plan)
        + render_compliance(plan)
    )
    page = HTML_TEMPLATE.replace("__TITLE__", esc(title))
    page = page.replace("__VARIANT__", esc(" — ".join(variant_bits)))
    page = page.replace("__CONTRIB__", contribution_banner_html())
    page = page.replace("__BODY__", body)
    return page


def main(argv: Optional[list[str]] = None) -> int:
    p = argparse.ArgumentParser(description="Render growth-plan.json to a self-contained HTML deliverable.")
    p.add_argument("plan", help="Path to growth-plan.json (assembled by the skill)")
    p.add_argument("--out", default="growth-plan.html", help="Output HTML path")
    args = p.parse_args(argv)

    with open(args.plan, encoding="utf-8") as fh:
        plan = json.load(fh)
    page = render(plan)
    with open(args.out, "w", encoding="utf-8") as fh:
        fh.write(page)
    print(json.dumps({"written": args.out,
                      "posts": sum(len(w.get("posts", [])) for w in plan.get("calendar", [])),
                      "metrics": len(plan.get("metrics", []))}))
    return 0


if __name__ == "__main__":
    sys.exit(main())
